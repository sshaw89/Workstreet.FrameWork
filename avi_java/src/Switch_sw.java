public class Switch_sw {

	public static void main(String[] args) 
	{
		int sig = 4;
		switch(sig)
		{
		case 1 : System.out.println("Stop ");
		         break;
		case 2 : System.out.println("Slow down ");
		         break;
		case 3 : System.out.println("Go ");
		         break;
		default : System.out.println("Chalan ghar bhej denge ");    
		}
		

	}

}